import google.generativeai as genai # Import the generative AI library
from google.colab import userdata # Import userdata to securely access API keys


# API key setup, very important for authenticating with the Gemini API
GOOGLE_API_KEY=userdata.get('GOOGLE_API_KEY') # Retrieve the API key from Colab secrets
genai.configure(api_key=GOOGLE_API_KEY) # Configure the genai library with the API key

# Initialize the Generative Model, specifying 'gemini-flash-latest' to use the most recent Flash model
gemini_model = genai.GenerativeModel('gemini-flash-latest') # Switched to gemini-flash-latest to avoid availability issues

# Loop through all available generative models to check which ones support content generation
for m in genai.list_models(): # Get a list of all models
  if 'generateContent' in m.supported_generation_methods: # Check if the model supports generating content
    print(m.name) # Print the name of the models that support content generation

"""Now, let's combine language detection with a simple prompt to the Gemini model to make it respond in the detected language."""

# Define a function to get a multilingual AI response
def multilingual_ai_response(u_in): # Takes user input 'u_in' as an argument
    d_lng = detect_language(u_in) # Detect the language of the user input
    if "Could not detect language" in d_lng: # Check if language detection failed
        print("Language detection failed!") # Oh no! Print an error message
        return f"Sorry, I couldn't understand the language of your input. Please try again. ({d_lng})" # Return an error response

    # Craft a prompt that encourages the model to respond in the detected language
    # This ensures the AI's response is in the same language as the user's query
    p_str = "You are an AI assistant. The user's input is in " + d_lng + ". Please respond to the following query in " + d_lng + ":\n\nUser: " + u_in + "\nAI:" # Construct the prompt string

    try:
        response = gemini_model.generate_content(p_str) # Send the prompt to the Gemini model to generate a response
        return response.text # Return the AI's generated text response
    except:
        return "An error occurred while generating a response, check your internet or API key!" # Handle any errors during response generation

# Test the multilingual AI response function with inputs in different languages
print("--- English Input ---") # Section header for English test
eng_q = "What is the capital of France?" # English query
print(f"Input: {eng_q}") # Print the input query
print(f"Response: {multilingual_ai_response(eng_q)}\n") # Print the AI's response

print("--- Spanish Input ---") # Section header for Spanish test
spa_q = "¿Cuál es la capital de España?" # Spanish query
print(f"Input: {spa_q}") # Print the input query
print(f"Response: {multilingual_ai_response(spa_q)}\n") # Print the AI's response


