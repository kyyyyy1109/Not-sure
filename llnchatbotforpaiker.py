print(f"Input: {fre_q}") # Print the input query
print(f"Response: {multilingual_ai_response(fre_q)}\n") # Print the AI's response

"""### Start a Conversation"""

# Start an interactive chat session with the AI bot
print("Start chatting with the bot! Type 'exit' or 'quit' to end the conversation.")
while True: # Start an infinite loop for continuous conversation
    u_in = input("You: ") # Get user input
    print("Processing your input...") # Added for fun, indicates the bot is thinking
    if u_in.lower() in ['exit', 'quit']: # Check if the user wants to end the conversation
        print("Bot: Goodbye!") # Bot says goodbye
        break # Exit the loop, ending the conversation

    b_resp = multilingual_ai_response(u_in) # Get a multilingual response from the AI for the user's input
    print(f"Bot: {b_resp}") # Print the AI's response

